/** Set of dark theme IDs — add future dark variants here */
const darkThemes: ReadonlySet<ThemeId> = new Set([
  'dark', 'ember', 'void', 'neon', 'twilight', 'carbon', 'abyss',
])

/** Is this a dark theme? Used to toggle dark-specific UI (e.g. hide ambient blobs) */
export function isDarkTheme(id: ThemeId): boolean {
  return darkThemes.has(id)
}

export function useTheme() {
  const [themeId, setThemeId] = useLocalStorage<ThemeId>(STORAGE_KEYS.theme, 'cool')

  useEffect(() => {
    const vars = themes[themeId]
    const root = document.documentElement
    for (const [key, val] of Object.entries(vars)) {
      root.style.setProperty(key, val)
    }
    const meta = document.querySelector('meta[name="theme-color"]')
    if (meta) meta.setAttribute('content', vars['--color-bg'])
  }, [themeId])

  return { themeId, setThemeId }
}
